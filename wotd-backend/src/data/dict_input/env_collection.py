from enum import Enum


class SensitiveEnv(str, Enum):
    ENV_PASSWORD = 'POSTGRES_PASSWORD'
    ENV_USER = 'POSTGRES_USER'
    ENV_DB_NAME = 'POSTGRES_DB'


class ConnectionEnv(str, Enum):
    ENV_DB_HOST = 'POSTGRES_HOST'
    ENV_DB_PORT = 'POSTGRES_PORT'
    ENV_ANKI_CONNECT_HOST = 'ANKI_CONNECT_HOST'
    ENV_ANKI_CONNECT_DATA_PORT = 'ANKI_CONNECT_DATA_PORT'
    ENV_ANKI_CONNECT_LOGIN_PORT = 'ANKI_CONNECT_LOGIN_PORT'


class GeneralEnv(str, Enum):
    ENV_WOTD_VERSION = 'WOTD_VERSION'
